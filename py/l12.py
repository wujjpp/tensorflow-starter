class Student(object):
    __slots__ = ('name', 'age')


class GraduateStudent(Student):
    pass


s = Student()
s.name = 'Jane'
s.age = 20

try:
    s.score = 99
except AttributeError as e:
    print('AttributeError:', e)

g = GraduateStudent()
g.score = 99
print('g.score =', g.score)


class Student2(object):

    @property
    def birth(self):
        return self._birth

    @birth.setter
    def birth(self, value):
        self._birth = value

    @property
    def age(self):
        return 2019 - self._birth


